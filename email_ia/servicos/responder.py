def generate_response(categoria):
    if categoria == 'Produtivo':
        return (
            "Obrigado por entrar em contato. Vamos analisar sua solicitação e responderemos em breve "
            "por nossa equipe. Em breve retornaremos com mais informações."
        )

    return (
        "Agradecemos sua mensagem! Ficamos à disposição para ajudar no que for necessário."
        " Se precisar de algo mais, não hesite em nos contatar."
    )